/**
 * Markdown export — single .md file with embedded base64 images.
 * Accepts AnalysedEntry[] (see core/schema.js); no app internals needed.
 *
 * @module export/markdown
 */

import {
  APP_NAME, SITE_URL, THRESHOLDS_FOOTER, DISCLAIMER_TEXT, checkInfoUrl,
  VESTIBULAR_CHECKER_URL
} from './strings.js';
import { pairChecks, wcagLine, advancedLine, pairBadges, statusWord, CHECK_GROUPS } from './checks.js';

function anchor(filename) {
  return filename.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
}

/**
 * Markdown pill: bold-bracketed label per Simon's design spec.
 * Screen readers on VS Code / Obsidian / GitHub announce "bold PASS" etc.,
 * which is unambiguous. Raw-text fallback reads as **[PASS]**, which still
 * carries the verdict clearly.
 */
function mdPill(status) {
  if (status === 'PASS' || status === 'SAFE') return '**[PASS]**';
  if (status === 'FAIL' || status === 'HIGH') return '**[FAIL]**';
  if (status === 'WARN' || status === 'HARSH') return '**[REVIEW]**';
  return '[NO TEXT]';
}

/**
 * Build the full Markdown string for a batch of analysed entries.
 *
 * @param {import('../core/schema.js').AnalysedEntry[]} entries
 * @param {string} timestamp
 * @returns {string}
 */
export function buildMarkdown(entries, timestamp) {
  const lines = [];

  lines.push('# Image Colour Contrast Audit');
  lines.push('');
  lines.push(`_Generated ${timestamp}_`);
  lines.push(`_Generated by [${APP_NAME}](${SITE_URL})_`);
  lines.push('');
  lines.push('---');
  lines.push('');
  lines.push(`> **Automated analysis only.** ${DISCLAIMER_TEXT.replace('This report is generated automatically to help speed up accessibility review. ', '')}`);
  lines.push('');
  lines.push('---');
  lines.push('');

  // ── Summary ───────────────────────────────────────────────────────────────
  lines.push('## Summary');
  lines.push('');
  lines.push('| Image | Result |');
  lines.push('|-------|--------|');
  for (const e of entries) {
    const resultPill = e.report.flag ? '**[FAIL]**' : (e.report.verdict === 'PASS' ? '**[PASS]**' : '[NO TEXT]');
    lines.push(`| [${e.filename}](#${anchor(e.filename)}) | ${resultPill} |`);
  }
  lines.push('');
  lines.push('---');
  lines.push('');
  lines.push('## Accessibility Detail');
  lines.push('');

  // ── Per-image detail ──────────────────────────────────────────────────────
  entries.forEach((entry) => {
    const { filename, report, previewDataUrl, pairAssets = [], cbSimAssets = [] } = entry;

    // Heading — no number prefix, matches PDF h2 and web UI h3
    lines.push(`### ${filename}`);
    lines.push('');

    // Preview — before result line, matching UI and PDF order
    if (previewDataUrl) {
      lines.push(`![Preview of ${filename}](${previewDataUrl})`);
      lines.push('');
    }

    // Result line
    const resultPill = report.flag ? '**[FAIL]**' : (report.verdict === 'PASS' ? '**[PASS]**' : '[NO TEXT]');
    lines.push(`**Result:** ${resultPill} — ${report.detail}`);
    lines.push('');

    // Colour-blindness simulation — before contrast results, matching UI and PDF order
    if (cbSimAssets.length) {
      lines.push('**Colour-blindness simulation**');
      lines.push('');
      lines.push('_How this image may appear to viewers with the most common colour-vision deficiencies._');
      lines.push('');
      for (const a of cbSimAssets) {
        lines.push(`![${a.label} — ${a.note}](${a.dataUrl})`);
        lines.push('');
        lines.push(`_${a.label} — ${a.note}_`);
        lines.push('');
      }
    }

    if (report.hasText && report.colourPairs.length) {
      lines.push('**Contrast results**');
      lines.push('');
      lines.push(`_${wcagLine(report)}_`);
      lines.push('');
      lines.push(`_${advancedLine(report)}_`);
      lines.push('');

      const assetByPair = new Map(pairAssets.map((a) => [a.pair, a]));
      for (const p of report.colourPairs) {
        const asset  = assetByPair.get(p);
        const words  = p.examples.map((e) => `"${e}"`).join(', ');
        const webaim = `https://webaim.org/resources/contrastchecker/?fcolor=${p.fgHex.slice(1)}&bcolor=${p.bgHex.slice(1)}`;
        // Inside <summary>, markdown is not processed by any parser — use plain HTML.
        // <strong> for bold badges, <code> for hex values (backticks are literal here).
        const badges = pairBadges(p).map((b) => `[${b.short} ${statusWord(b.status).toUpperCase()}]`).join(' · ');

        // Swatch is embedded inline in the summary so the colours are visible
        // without expanding the block. height="14" keeps it flush with the text.
        const swatchTag = asset?.swatchDataUrl
          ? `<img src="${asset.swatchDataUrl}" alt="${p.bgHex} on ${p.fgHex}" height="14"> `
          : '';
        lines.push('<details open>');
        lines.push(`<summary>${swatchTag}<strong>${badges}</strong> — <code>${p.bgHex}</code> background / <code>${p.fgHex}</code> foreground${words ? ` — ${words}` : ''}</summary>`);
        lines.push('');
        lines.push(`[Check this pair on WebAIM](${webaim})`);
        lines.push('');
        lines.push(`[Vestibular Accessible Design Checker](${VESTIBULAR_CHECKER_URL})`);
        lines.push('');
        lines.push('| Check | Value | Status | What it means |');
        lines.push('|-------|-------|--------|---------------|');
        const checks = pairChecks(p);
        for (const grp of CHECK_GROUPS) {
          lines.push(`| **${grp.label}** | | | |`);
          for (const c of checks.filter((check) => check.group === grp.id)) {
            lines.push(`| [${c.label}](${checkInfoUrl(c.id)}) | ${c.value || '—'} | ${mdPill(c.status)} | ${c.detail} |`);
          }
        }
        lines.push('');
        if (asset?.clipDataUrl) {
          lines.push('Where this combination appears in the image:');
          lines.push('');
          lines.push(`![Cropped image region](${asset.clipDataUrl})`);
          lines.push('');
        }
        lines.push('</details>');
        lines.push('');
      }
    }

    lines.push('');
  });

  // ── Footer ────────────────────────────────────────────────────────────────
  lines.push('---');
  lines.push('');
  lines.push(`> ${THRESHOLDS_FOOTER}`);
  lines.push(`> Generated by [${APP_NAME}](${SITE_URL})`);
  lines.push('');

  return lines.join('\n');
}

/**
 * Trigger a browser download of the Markdown report.
 *
 * @param {string} markdown
 * @param {string} filename
 */
export function downloadMarkdown(markdown, filename) {
  const blob = new Blob([markdown], { type: 'text/markdown;charset=utf-8' });
  const url  = URL.createObjectURL(blob);
  const a    = Object.assign(document.createElement('a'), { href: url, download: filename });
  document.body.append(a);
  a.click();
  a.remove();
  setTimeout(() => URL.revokeObjectURL(url), 1000);
}
